package TDAGrafo;

public class prueba {
	private int numerito;
	private String nombre;
	private char k;
	public prueba(String nombre, char edad, int num) {
		numerito=num;
		this.nombre=nombre;
		edad=k;
	}
	public int getNumerito() {
		return numerito;
	}
	public String getNombre() {
		return nombre;
	}
}


